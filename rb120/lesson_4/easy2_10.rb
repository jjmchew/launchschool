Benefits of OOP in Ruby
  - name-spacing - keep methods, variables, etc. distinct and prevent confusion
  - inheritance - manage related classes and inherited attributes and behaviours
  - ability to mixin additional behaviours
  - ability to define how created objects look, interact, respond in different circumstances
  - can be easier to add functionality and prevent conflict between different parts of code-base

  - can think more abstractly - makes code more flexible
  - objects are represented by 'nouns' - can be easier to conceptualize
  - can manage method control access
  - can easily create same functionality in different parts of code with no duplication
  - easier to use pre-written code
  - easier to manage complexity
